create table t_email (
	id bigint generated by default as identity,
	name varchar(128) not null,
	title varchar(512) not null,
  source varchar(256),
  type varchar(256),
	sent_date VARCHAR(16),
	file_name VARCHAR(512),
  primary key (id)
);

DROP table t_search_key;
create table t_search_key(
  id bigint generated by default as identity,
  key varchar(256),
	search_condition VARCHAR(1024),
	total int,
	max int,
	page int,
	record_count int,
	last_refresh date,
	primary key (id)
);

